public class TiposPrimitivos {
    public static void main(String[] args) {
        byte a = 0b10; // 1 byte
        short b = 10; // 2 bytes
        int c = 10; // 4 bytes
        long d = 10; // 8 bytes

        float e = 10.0f; // 4 bytes
        double f = 20.0; // 8 bytes

        boolean g = true;
        char h = 'h';

        System.out.printf("%d", a);
    }
}